package no.abakus.bedcard.storage.ws;

public class AbakusNoException extends Exception {

	public AbakusNoException() {
		super();
	}

	public AbakusNoException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public AbakusNoException(String arg0) {
		super(arg0);
	}

	public AbakusNoException(Throwable arg0) {
		super(arg0);
	}

}
